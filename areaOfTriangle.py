#area of a triangle 
# we can fine the area of a triangle with the following formula :
# A = H*B /2 

H = float (input ("Please enter the height of the triangle in centimeters: ")) 
B = float (input ("Please enter the base of the triangle in centimeters: "))

A = (H*B) / 2 

print(f"The area of the triangle is {A} Square centimeters (cm2) " )